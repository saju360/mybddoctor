    private fun handleIntent(intent: android.content.Intent) {
        val target = intent.getStringExtra("navigate_to")
            ?: when(intent.action) {
                "OPEN_EMERGENCY" -> "emergency"
                "OPEN_BLOOD_REQUEST" -> "blood_request"
                "OPEN_BLOOD" -> "blood"
                else -> null
            }
        if (target != null) {
            navigateTo = target
        }
    }

    private fun routeValue(value: Any?): String = Uri.encode(value?.toString().orEmpty())

    private fun appointmentRoute(doctorId: Long? = null, doctorName: String? = null, specialty: String? = null): String =
        "appointments?doctorId=${doctorId ?: 0L}&doctorName=${routeValue(doctorName)}&specialty=${routeValue(specialty)}"

    private fun detailsRoute(
        title: String,
        subtitle: String,
        type: String,
        phone: String? = "",
        address: String? = "",
        entityId: Any? = -1L,
        donorId: Any? = -1L,
        history: String? = "",
        rewards: Any? = 0,
        hours: String? = ""
    ): String =
        "details?title=${routeValue(title)}&subtitle=${routeValue(subtitle)}&type=${routeValue(type)}" +
            "&phone=${routeValue(phone)}&address=${routeValue(address)}&entityId=${entityId ?: -1L}" +
            "&donorId=${donorId ?: -1L}&history=${routeValue(history)}&rewards=${rewards ?: 0}" +
            "&hours=${routeValue(hours)}"

